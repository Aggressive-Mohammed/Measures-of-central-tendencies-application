    private void jMenuItem7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem7ActionPerformed
      Scanner input = new Scanner(System.in);
       
       double size;
       ArrayList<Integer> list = new ArrayList();
       
       double median=0;
       
       size= Integer.parseInt(JOptionPane.showInputDialog(null,"How many numbers are they? ")); 
       
       
        for(int counter=0; counter<size; counter++)
        {
             list.add(Integer.parseInt(JOptionPane.showInputDialog("Enter the Number :  "+(counter+1)))); 
              
        }
        
        
        JOptionPane.showMessageDialog(null, "Your numbers are :"+ list);
        
        Collections.sort(list);
        
        
         size = Math.ceil(size/2);
         
       if(size%2==0)
       {
            Double m = size;
            int medi = m.intValue();
        
         
            double firstMidValue = list.get(medi-1);
            double secondMidValue=list.get(medi);
     
         
            median = ((firstMidValue)+(secondMidValue))/2;
        
                             
     
         JOptionPane.showMessageDialog(null, " The median is : "+ median );
       }
       else
       {
              Double m = size;
         
              int medi = m.intValue();
         
               JOptionPane.showMessageDialog(null, " The median is : "+ list.get(medi));  
       }
        
        

          
    }//GEN-LAST:event_jMenuItem7ActionPerformed

