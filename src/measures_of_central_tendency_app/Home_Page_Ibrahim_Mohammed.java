    private void jMenuItem5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem5ActionPerformed
        
    Scanner input = new Scanner(System.in);
    
    
    
    int size= 0;
    
   size = Integer.parseInt(JOptionPane.showInputDialog(null,"How many are your numbers? ")) ;

    
   
    
    int[] dataSet =  new int[size];
    
    for(int num =0; num<size; num++)
    {
        
        dataSet[num]+=Integer.parseInt( JOptionPane.showInputDialog(null,"Enter the numbers : "+ (num+1)));
         
    }
    
        
    int maxNumber = -1;
    int maxAppearance = -1;
    for(int i = 0; i<dataSet.length; i++ )
    {
       
         int count = 0;
         
         for(int j=0; j<dataSet.length; j++)
            {
             
            if(dataSet[i] == dataSet[j])
                count++;
            
          
            }
           if(count>maxAppearance)
           {
                maxNumber = dataSet[i];
                maxAppearance=count; 
            
           }
          
         
    }
    
     JOptionPane.showMessageDialog(null,"The mode is "+ maxNumber );   
    

       
    }//GEN-LAST:event_jMenuItem5ActionPerformed

