    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       int numberOfRows = varianceTable.getRowCount();
       
       double sumOfFirstRow=0;
       double sumOfSecondRow= 0;
       double variance= 0;
       double standardDeviation=0;
       double mean =0;
       double cOf=0;
       
       for(int i=0; i<numberOfRows; i++)
       {
          double valuesOnColunmOne=Double.valueOf(varianceTable.getValueAt(i, 0).toString());
          sumOfFirstRow +=valuesOnColunmOne;
          
          double valuesOnColunmTwo= Math.pow(Double.valueOf(varianceTable.getValueAt(i, 0).toString()),2);
          sumOfSecondRow+=valuesOnColunmTwo;
          
          mean= sumOfFirstRow/numberOfRows;
          
      variance =  (sumOfSecondRow/numberOfRows) - (Math.pow((sumOfFirstRow/numberOfRows),2));
      
      standardDeviation=Math.sqrt(variance);
      
      cOf = (standardDeviation/mean);
          
       }
       
       jTextField1.setText(Double.toString(cOf)+" %");
      
    }//GEN-LAST:event_jButton1ActionPerformed

